- (void)showRoute {
    UIBarButtonItem *rightBarButton = [[UIBarButtonItem alloc] initWithTitle:@"到这里" style:UIBarButtonItemStylePlain target:self action:@selector(showTypeActionSheet)];
    self.navigationItem.rightBarButtonItem = rightBarButton;
}

- (void)showTypeActionSheet {
    UIActionSheet *actionSheet = [[UIActionSheet alloc] initWithTitle:@"选择方式" delegate:self cancelButtonTitle:@"取消" destructiveButtonTitle:nil otherButtonTitles:@"公交", @"汽车", @"步行", nil];
    [actionSheet showInView:self.view];
}

- (void)actionSheet:(UIActionSheet *)actionSheet clickedButtonAtIndex:(NSInteger)buttonIndex
{
    if (actionSheet.firstOtherButtonIndex == buttonIndex) {
        [self byBus];
    } else if (actionSheet.firstOtherButtonIndex + 1 == buttonIndex) {
        [self byCar];
    } else if (actionSheet.firstOtherButtonIndex + 2 == buttonIndex) {
        [self byWalk];
    }
}

